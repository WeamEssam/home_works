void main() {
  // Write a Dart code snippet that demonstrates the use of an arithmetic operator to
  // calculate the remainder of dividing 10 by 3.

  int number1 = 10;
  int number2 = 3;
  int remainderResult = number1 % number2; // 10 % 3 =1
  print(remainderResult);

  double divisionResult = number1 / number2; // 10 / 3 =3.33
  print(divisionResult);
}
